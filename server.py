ip   = '127.0.0.1'
port = 5000
server = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
server.bind((ip, port))
print(f"[*] UDP server รอรับข้อมูลที่ {ip}:{port}")
print(f"[*] key ที่รับได้: {', '.join(predictions.keys())}")

while True:
    data, client_addr = server.recvfrom(1024)
    message = data.decode('utf-8').strip().lower()
    print(f"[>] รับ: '{message}' จาก {client_addr}")

    if message == "exit":
        print("[*] Server ปิดการทำงาน")
        server.sendto("bye".encode('utf-8'), client_addr)
        break

    if message in predictions:
        response = random.choice(predictions[message])
    else:
        response = f"Try Again (key ที่รับได้: {', '.join(predictions.keys())})"

    print(f"[<] ส่ง: {response}")
    server.sendto(response.encode('utf-8'), client_addr)

server.close()
