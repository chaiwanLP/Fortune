NAKSAT_CYCLE = [
    ("rat",     "ชวด"),
    ("ox",      "ฉลู"),
    ("tiger",   "ขาล"),
    ("rabbit",  "เถาะ"),
    ("dragon",  "มะโรง"),
    ("snake",   "มะเส็ง"),
    ("horse",   "มะเมีย"),
    ("goat",    "มะแม"),
    ("monkey",  "วอก"),
    ("rooster", "ระกา"),
    ("dog",     "จอ"),
    ("pig",     "กุน"),
]
NAKSAT_BASE_YEAR = 2020  # ปี 2020 = ปีชวด


def get_zodiac(day: int, month: int):
    """คืนค่า (zodiac_key, zodiac_name_th)"""
    for (m1, d1), (m2, d2), key, name in ZODIAC_RANGES:
        # กรณีข้ามปี เช่น มังกร (12/22 - 1/19)
        if m1 > m2:
            if (month == m1 and day >= d1) or (month == m2 and day <= d2):
                return key, name
        else:
            if (month == m1 and day >= d1) or (month > m1 and month < m2) or (month == m2 and day <= d2):
                return key, name
    return "capricorn", "มังกร"  # fallback


def get_naksat(year: int):
    """คืนค่า (naksat_key, naksat_name_th)"""
    idx = (year - NAKSAT_BASE_YEAR) % 12
    key, name = NAKSAT_CYCLE[idx]
    return key, name


def get_fortune(category: str, day: int, month: int, year: int) -> str:
    """
    คำนวณราศี + ปีนักษัตร แล้วเลือกคำทำนาย
    - ถ้ามี special_predictions ที่ตรงกับ zodiac+naksat+category → ใช้อันนั้น
    - ถ้าไม่มี → fallback ไปใช้ predictions[category]
    """
    zodiac_key, zodiac_name = get_zodiac(day, month)
    naksat_key, naksat_name = get_naksat(year)

