SERVER_IP   = "0.0.0.0"
SERVER_PORT = 5000

server = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
server.bind((SERVER_IP, SERVER_PORT))
print(f"[*] Fortune Server พร้อมทำงานที่ {SERVER_IP}:{SERVER_PORT}")
print(f"[*] รูปแบบ request: category|dd|mm|yyyy  เช่น  love|15|8|1999")

while True:
    data, client_addr = server.recvfrom(1024)
    message = data.decode("utf-8").strip()
    print(f"[>] รับ: '{message}' จาก {client_addr}")

    if message.lower() == "exit":
        server.sendto("bye".encode("utf-8"), client_addr)
        print("[*] Server ปิดการทำงาน")
        break

    # parse: category|dd|mm|yyyy
    parts = message.split("|")
    if len(parts) == 4:
        category_raw, dd, mm, yyyy = parts
        category = category_raw.strip().lower()
        try:
            day   = int(dd)
            month = int(mm)
            year  = int(yyyy)
            response = get_fortune(category, day, month, year)
        except ValueError:
            response = "รูปแบบวันเกิดไม่ถูกต้อง กรุณาส่ง: category|dd|mm|yyyy"
    else:
        response = "รูปแบบไม่ถูกต้อง กรุณาส่ง: category|dd|mm|yyyy  เช่น  love|15|8|1999"

    print(f"[<] ส่ง: {response}")
    server.sendto(response.encode("utf-8"), client_addr)

server.close()